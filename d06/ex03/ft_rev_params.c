void	ft_putchar(char c);

void	ft_putstr(char *str)
{
	while (*str)
	{
		ft_putchar(*str);
		str++;
	}
}

int		main(int argc, char **argv)
{
	int i;

	i = argc - 1;
	while (i)
	{
		ft_putstr(argv[i]);
		ft_putchar(10);
		i--;
	}
	return (0);
}
